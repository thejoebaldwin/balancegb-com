<H1 CLASS="western" STYLE="margin-left: 1in; text-indent: 0.5in">A

Positive Resolution</H1>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	The way that you live

your life truly depends on your perspective.  Where one person sees a

travesty another sees an opportunity.  If Jackie Robinson thought

that he couldn&rsquo;t make a difference he would have never broken

the color barrier of Major League Baseball.  Being more positive

minded and optimistic is something that all of us can strive to be

better at for our mental and physical health.  I love the way a

colleague of mine, Dr. Larry Markson, put it.  &ldquo;You can always

be more optimistic until you can change the tire on a stranger&rsquo;s

car with the same enthusiasm that you open your Christmas presents

with.&rdquo;</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	Optimism is what most

people think of as a &ldquo;glass half full&rdquo; mentality.  Make

sure that you don&rsquo;t confuse optimism with blind faith of all

good things.  Optimism is not convincing yourself that things are

great when they aren&rsquo;t.  In stead, optimists have a positive

view of their life and their ability to affect the future.  This

mentality lends itself to directing energy towards the factors that

can be changed and ignoring those that cannot be changed.  Optimists

respond to a situation as empowering as possible and best utilize all

of their available resources.  Most optimists feel as if they have a

true purpose in life, set goals to achieve that purpose and have the

ability to put all situations in perspective.   

</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	A positive attitude

also lends itself to the law of probability.  The law of probability

states that the more things that you try the greater the chance that

one of them will make a difference.  By utilizing opportunities all

around them and creating more positive prospects optimists have a

greater chance of learning something, meeting someone or trying

something that will make a difference.  

</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	We live in an overly

pessimistic society.  Tune into the nightly news and you undoubtedly

will be flooded with negative words and pictures.  In the last 30

years there have been 46,000 published psychological papers on

depression and only 400 on joy.  Pessimists often focus on what they

don&rsquo;t have and what they can&rsquo;t change thus producing more

of what they don&rsquo;t have.  Nothing has any meaning but the

meaning that you give it.  Pessimists often give negative meaning to

events and invite more negativity into their life.  You often hear

pessimists using the words should, try, can&rsquo;t and have to.</P>

<P CLASS="western" STYLE="text-indent: 0.5in; margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="text-indent: 0.5in; margin-bottom: 0in">Science

has established a strong correlation between optimism and self-esteem

and psychological well being.  Research has suggested that optimists

experience less stress and may have more resources to deal with the

stress they do have.  Considering that 1 in 10 American teenagers

(2.2 million) experienced major depression last year isn&rsquo;t

optimism something that we should be teaching all of our children? 

Suprisingly, a recent study found that out of ten optimistic traits

identified, two were consistently taught in public schools. 

</P>

<P CLASS="western" STYLE="text-indent: 0.5in; margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="text-indent: 0.5in; margin-bottom: 0in">Not

only does optimism positively affect mental health, it also can make

a tremendous impact on physical health.  Here is a brief list of

research that suggests optimism can increase the health and well

being of your family.</P>

<UL>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Pessimists have

	been found to have decreased immunocompetence (the ability to fight

	off infection) in response to stress.</P>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Levels of

	pessimism have been found to be a significant predictor in mortality

	of patients with HIV and children with recurrent cancer. 

	</P>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Pessimism has been

	linked to a higher risk of dying before age 65.</P>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Positive thinking

	has been shown to decrease the production of the stress hormone

	cortisol.</P>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">The Journal of

	Psychosomatic Medicine published an article showing that optimistic

	men has less than half the risk of heart disease than pessimistic

	men.</P>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Pessimists that

	were studied after heart surgery were significantly more likely to

	have two predictive factors for a future heart attack. The optimists

	that were studied at a six-moth checkup were more likely to have

	resumed physical exercise and be working. A five-year checkup

	revealed that optimists were less likely to rehospitalized for

	complications and reported less chest pain.</P>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Subjects that were

	found to be &ldquo;very optimistic&rdquo; in one study had a 55%

	lower risk of death from all causes.</P>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Optimism is

	associated with more physical activity, moderate alcohol use in

	women and reduced smoking.    

	</P>

</UL>

<P CLASS="western" STYLE="text-indent: 0.5in; margin-bottom: 0in"> 

