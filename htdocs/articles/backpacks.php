<H1 CLASS="western"><FONT SIZE=4>BACKPACK SAFETY</FONT></H1>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">With back to school

right around the corner I&rsquo;m reminded of something that concerns

me about the children in our community.  As I watch children walking

home from school a majority of them are lugging backpacks that are

too heavy, fit them poorly, and contort their bodies.  Add the fact

that it is becoming increasingly popular to have one-strapped packs

or to have a backpack slung over one shoulder and you have a recipe

for problems.</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">According to the U.S.

product and safety commission, in 2003 alone <B>21,000</B> people

were treated in emergency rooms, doctor&rsquo;s offices and clinics

for backpack related injuries.  In many cases backpack misuse sets

the groundwork for problems that do not show up until years later.

Another scary fact is that backpack misuse leads to long lasting

chronic problems in millions of children.  I see children in my

practice every day that have high shoulders, low hips and unnatural

spinal curvatures that can be caused and accentuated by poor backpack

use.  Backpack misuse leads to the spine being uneven which in turn

produces nervous system (the master control of the body) imbalance. 

This can show up as pain, abnormal sensation, muscle inequality or

improper organ control.   

</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	A recent study in

Italy found that the average child carries a backpack that would be

equivalent to a 39-pound burden on a 176-pound man, or a 29-pound

load for a 132-pound woman.  8 percent of the children studied carry

more than 30 percent of their body weight at least once a week. 

Also, the study found that of the children carrying heavy backpacks,

60 percent had experienced back pain as a direct result.  Another

Italian study found that 79.1 percent of children feel that their

backpacks are too heavy, 65.7 percent felt fatigued due to carrying

their backpack and 46.1 percent had experienced back pain after

wearing a backpack.</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	Chiropractors, medical

doctors, orthopedists and physical therapists nationwide have seen a

recent increase in children presenting with back and shoulder

complaints.  This problem has become so prevalent that several states

including California and New Jersey have recently passed legislation

to force school districts to develop ways of reducing the weight of

students&rsquo; backpacks.</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

