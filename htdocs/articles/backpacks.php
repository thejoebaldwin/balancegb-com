<H1 CLASS="western">TIPS TO PREVENT BACKPACK MISSUSE</H1>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<UL>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Backpack should

	weigh no more than <B>10 percent</B> of a child&rsquo;s body weight.

	 A heavier backpack will cause your child to compensate by leaning

	forward.  This posture over time can lead to neck, back and shoulder

	pain.  

	</P>

</UL>

<P CLASS="western" STYLE="margin-left: 0.25in; margin-bottom: 0in"><BR>

</P>

<UL>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">The backpack

	should never hang more than <B>4 inches</B> below the waistline. 

	The lower a backpack hangs the more it increases pressure on a

	child&rsquo;s shoulders.  This also leads to a leaning forward

	posture. 

	</P>

</UL>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<UL>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Urge your child to

	<B>wear both shoulder straps</B>.  Avoid all one-strap bags. 

	Educate your children that if they wear only one strap they will end

	up with one shoulder being 6 inches higher than the other and a

	spinal curve to the opposite side.  That is not cool!</P>

</UL>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<UL>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in"><B>Wide, padded

	shoulder straps</B> are important.  Non-padded straps dig into a

	child&rsquo;s shoulders and lead to poor posture.  Backpack should

	have <B>adjustable shoulder straps</B> to ensure that the backpack

	can be fitted to your child&rsquo;s body. 

	</P>

</UL>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<UL>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Lumbar curve

	support and hip belts are an added plus that help distribute more

	weight to the pelvis.  This supports the body&rsquo;s natural

	posture and spinal curves.</P>

</UL>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<UL>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Always pack

	heavier items closest to the child&rsquo;s back.  Pack pointy or

	bulky objects away from the child&rsquo;s back.  Pack to assure that

	items do not shift around.</P>

</UL>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<UL>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Choose lightweight

	backpacks with multiple compartments to allow even distribution of

	weight throughout the backpack.</P>

</UL>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<UL>

	<LI><P CLASS="western" STYLE="margin-bottom: 0in">Look for

	ergonomically designed backpacks like the AirPack that I offer my

	patients.  The Air pack has been shown in studies to reduce the

	effective load on a child&rsquo;s spine up to 80%.</P>

</UL>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P STYLE="text-indent: 0.5in; margin-bottom: 0in">Possibly the most

important tip that I can give parents on backpack safety is to pay

close attention to your child each day they are wearing a backpack. 

They should be able to maintain their natural posture while wearing

it.  This means that they should have their ears over their shoulders

(that are also over theirs hips and ankles).  Correct posture also

means that their hips and shoulders should be at equal levels.  Any

sustained deviation from this posture should be investigated by a

healthcare professional, hopefully before symptoms show up. 

Remember, as with most things it&rsquo;s easier to prevent these

problems before your child has back pain.  

</P>

