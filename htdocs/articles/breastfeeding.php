</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	According to a recent

study, 14 percent of American mothers breastfeed exclusively for the

first 6 months.  It has been recommended by the World Health

Organization that mothers give their babies breast milk only until

they are 6 months old, and yet 86 percent of mothers do not. 

Furthermore, only six states in the U.S. met the WHO state guideline

that 50 percent of mothers breastfeed in part for 6 months.  How have

we gone so wrong from an estimated 85 percent of pregnant women that

intend to breastfeed?     

</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	The answer is a

combination of social stress, physical stress, lack of support and

lack of education.  Many expectant mothers have family members that

are telling them that breastfeeding &ldquo;doesn&rsquo;t make that

much of a difference&rdquo; or &ldquo;you were raised on formula and

you turned out just fine.&rdquo;  Many new mothers are simply not

ready for the trials and tribulations of breastfeeding and have not

made a solid commitment to stick with it.  The public at large is

undereducated on the overwhelming information that supports

breastfeeding.  Lastly, free formula at the hospital provides an easy

alternative when the going gets tough.</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<H2 CLASS="western">How the Baby Benefits</H2>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	Breastfeeding is a

natural continuation of the miracle of conception, pregnancy and

birth.  Breast milk is packed with intelligence, it is unique for

your infant and it&rsquo;s benefits can not be duplicated by any

other substance (Most authorities agree that formula doesn&rsquo;t

even come close).</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	The make up of breast

milk changes daily, hourly and even within a single feeding.  Breast

milk changes to provide the infant more water in warm weather and

more protein, fat and calories during their growth spurts.  Breast

milk is much easier to digest because it contains prostaglandins,

enzymes (including lipase), low sodium (which reduces stress on the

kidneys) and low protein (mostly highly digestible whey protein). 

Breast milk contains high amounts of vitamin C and zinc (along with a

zinc binding agent that makes it easier for absorption).  Eye and

brain development is promoted by two amino acids (that are not found

on cow&rsquo;s milk) and DHA. 

</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	Built in immunity is

possibly the most important gift within breast milk.  Anti-infective

agents including antibodies, white blood cells, lactoferrin, enzymes

and agents that energize the infant&rsquo;s immune response are

contained in colostrum and breast milk.  Lysozyme is 5000 times more

concentrated in human milk verses cow milk.  When a mother interacts

with a disease-causing agent in her environment she produces an IgA

antibody that is passed to the infant through breast milk.  IgA then

protects the infant&rsquo;s body against the same agent and ignores

bacteria that are normally found in the gut.  None of the blind

destruction of antibiotics is seen here, a mother&rsquo;s antibodies

are incredibly intelligent and specific. 

</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

</P>

<P CLASS="western" STYLE="margin-bottom: 0in">	Breastfed babies have

a lower incidence of allergies, diabetes, obesity, SIDS, childhood

cancer, Chron&rsquo;s disease, constipation, diarrhea, diaper rash,

respiratory tract infections, middle ear infections and bacterial

meningitis.  Breastfed babies have lower cholesterol, better mouth

development, and fewer cavities.  Breastfed babies have higher

cognitive ability and educational achievement!</P>

<P CLASS="western" STYLE="margin-bottom: 0in"><BR>

